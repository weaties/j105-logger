"""STYC results provider (#459).

Scrapes the static HTML generated by Sloop Tavern YC's scoring tool.
A regatta has:
- ``series.htm`` — per-class series standings (total/net points, place, OA)
- ``race1.htm`` … ``raceN.htm`` — per-race detail (start, finish, elapsed,
  corrected, place per class)

The provider takes an ``httpx.AsyncClient`` for testability.
"""

from __future__ import annotations

import html
import re
from dataclasses import dataclass
from datetime import datetime
from typing import TYPE_CHECKING, Any
from urllib.parse import urlparse, urlunparse

from loguru import logger
from selectolax.parser import HTMLParser

from helmlog.results.base import (
    BoatFinish,
    RaceData,
    Regatta,
    RegattaResults,
    SeriesStanding,
)

if TYPE_CHECKING:
    import httpx

_TIMEOUT = 30.0


@dataclass(frozen=True)
class StycDiscovery:
    """Result of auto-discovering a STYC regatta from a single URL."""

    source: str
    source_id: str
    name: str
    url: str


def _base_url_from(url: str) -> str:
    """Strip any ``raceN.htm``/``series.htm`` filename and query/fragment,
    returning the regatta base directory without a trailing slash.
    """
    parsed = urlparse(url)
    path = parsed.path
    if path.endswith(".htm") or path.endswith(".html"):
        path = path.rsplit("/", 1)[0]
    path = path.rstrip("/")
    return urlunparse((parsed.scheme, parsed.netloc, path, "", "", ""))


def _source_id_from(base_url: str) -> str:
    """Derive a stable, human-readable source_id from the URL path.

    ``.../race_info/Ballard_Cup/SeriesI/2026`` → ``Ballard_Cup_SeriesI_2026``
    """
    path = urlparse(base_url).path.strip("/")
    parts = [p for p in path.split("/") if p and p != "race_info"]
    return "_".join(parts) or "styc_regatta"


def _extract_racetitle(html_text: str) -> str:
    """Extract regatta name from ``<p class=racetitle>...</p>``."""
    m = re.search(
        r"<p[^>]*class=[\"']?racetitle[\"']?[^>]*>([^<]+)</p>",
        html_text,
        re.IGNORECASE,
    )
    return _decode(m.group(1)).strip() if m else ""


async def discover_styc_url(
    url: str,
    client: httpx.AsyncClient,
) -> StycDiscovery:
    """Auto-discover a STYC regatta from any page URL in its directory.

    Accepts a race page (``raceN.htm``), series page, or the base directory
    itself. Returns a populated ``StycDiscovery`` the admin UI can drop
    straight into the Add Regatta form.
    """
    base_url = _base_url_from(url)
    if not base_url or "styc.org" not in urlparse(base_url).netloc:
        raise ValueError(f"Not a STYC results URL: {url!r}")

    source_id = _source_id_from(base_url)

    # Prefer whatever the user actually pasted — it's the page most likely
    # to exist. Fall back to series.htm then race1.htm.
    candidates: list[str] = []
    if url not in candidates:
        candidates.append(url)
    candidates.append(f"{base_url}/series.htm")
    candidates.append(f"{base_url}/race1.htm")

    name = ""
    last_error: Exception | None = None
    for candidate in candidates:
        try:
            resp = await client.get(candidate, timeout=_TIMEOUT)
            resp.raise_for_status()
        except Exception as exc:  # noqa: BLE001
            last_error = exc
            continue
        title = _extract_racetitle(resp.text)
        if title:
            name = title
            break

    if not name:
        if last_error is not None:
            raise ValueError(f"Could not discover STYC regatta at {base_url!r}: {last_error}")
        # Fall back to the derived source_id prettified.
        name = source_id.replace("_", " ")

    logger.debug("STYC discover {} → {!r} @ {}", url, name, base_url)
    return StycDiscovery(
        source="styc",
        source_id=source_id,
        name=name,
        url=base_url,
    )


class StycProvider:
    """Fetch race results from STYC's static HTML pages."""

    source_name: str = "styc"

    def __init__(self, client: httpx.AsyncClient | None = None) -> None:
        self._client = client

    def _http(self) -> httpx.AsyncClient:
        if self._client is None:
            raise RuntimeError("StycProvider requires an httpx.AsyncClient")
        return self._client

    async def fetch(self, regatta: Regatta) -> RegattaResults:
        """Fetch all race and series data for a STYC regatta.

        ``regatta.url`` must be the base URL for the regatta's result
        directory (e.g. ``https://race.styc.org/race_info/.../2025/``),
        ending with a slash.  ``regatta.source_id`` is a unique identifier
        chosen by the admin (e.g. ``Ballard_Cup_SeriesI_2025``).
        """
        base_url = (regatta.url or "").rstrip("/")
        if not base_url:
            raise ValueError(f"STYC regatta {regatta.source_id!r} has no URL configured")

        # series.htm is only published once standings exist (usually after
        # race 2).  Early in a series — or with a single published race —
        # it 404s; fall back to probing raceN.htm until we hit a miss.
        standings: list[SeriesStanding] = []
        race_nums: list[int] = []
        series_html = await self._get_optional(f"{base_url}/series.htm")
        if series_html is not None:
            standings = _parse_series(series_html, regatta.source_id)
            race_nums = _discover_race_numbers(series_html)

        races: list[RaceData] = []
        if race_nums:
            for race_num in race_nums:
                race_html = await self._get(f"{base_url}/race{race_num}.htm")
                races.extend(_parse_race_page(race_html, regatta.source_id, race_num))
        else:
            # Probe race1..N until we hit a gap.
            for race_num in range(1, 100):
                maybe_html = await self._get_optional(f"{base_url}/race{race_num}.htm")
                if maybe_html is None:
                    break
                races.extend(_parse_race_page(maybe_html, regatta.source_id, race_num))
            if not races:
                raise ValueError(
                    f"STYC regatta at {base_url!r} has no published races yet "
                    f"(neither series.htm nor race1.htm found)"
                )

        return RegattaResults(
            regatta=regatta,
            races=tuple(races),
            standings=tuple(standings),
        )

    async def _get(self, url: str) -> str:
        resp = await self._http().get(url, timeout=_TIMEOUT)
        resp.raise_for_status()
        return resp.text

    async def _get_optional(self, url: str) -> str | None:
        """Fetch a page, returning None on 404 instead of raising."""
        import httpx as _httpx

        try:
            resp = await self._http().get(url, timeout=_TIMEOUT)
        except _httpx.HTTPError:
            return None
        if resp.status_code == 404:
            return None
        resp.raise_for_status()
        return resp.text


def _decode(text: str) -> str:
    """Decode HTML entities (&#58; → :, &#40; → (, etc.)."""
    return html.unescape(text)


def _cell_str(
    cells: list[Any],
    col: dict[str, int],
    key: str,
) -> str | None:
    """Read a decoded text cell, or None if key missing."""
    if key not in col:
        return None
    return _decode(cells[col[key]].text(strip=True))


def _cell_int(
    cells: list[Any],
    col: dict[str, int],
    key: str,
) -> int | None:
    val = _cell_str(cells, col, key)
    return _parse_int(val) if val else None


def _cell_float(
    cells: list[Any],
    col: dict[str, int],
    key: str,
) -> float | None:
    val = _cell_str(cells, col, key)
    return _parse_float(val) if val else None


def _discover_race_numbers(series_html: str) -> list[int]:
    """Extract race numbers from the series page header row."""
    matches = re.findall(r"Race\s+(\d+)", series_html)
    return sorted({int(m) for m in matches})


def _parse_series(series_html: str, source_id: str) -> list[SeriesStanding]:
    """Parse series.htm into per-boat standings."""
    tree = HTMLParser(series_html)
    standings: list[SeriesStanding] = []

    class_titles = tree.css("p.classtitle")
    class_tables = [
        t
        for t in tree.css("table")
        if t.css("th")
        and "sailno" in " ".join(_decode(th.text(strip=True)).lower() for th in t.css("th"))
    ]

    for idx, table in enumerate(class_tables):
        class_name = _decode(class_titles[idx].text(strip=True)) if idx < len(class_titles) else ""

        rows = table.css("tr")
        if len(rows) < 2:
            continue

        headers = [_decode(th.text(strip=True)).lower() for th in rows[0].css("th")]
        col = _header_map(headers)

        for row in rows[1:]:
            cells = row.css("td")
            if len(cells) < len(headers):
                continue

            sail = _decode(cells[col["sailno"]].text(strip=True))
            if not sail:
                continue

            total = _cell_float(cells, col, "total")
            place = _cell_int(cells, col, "place")
            oa = _cell_int(cells, col, "oa")

            standings.append(
                SeriesStanding(
                    sail_number=sail,
                    class_name=class_name,
                    total_points=total,
                    net_points=total,
                    place_in_class=place,
                    place_overall=oa,
                )
            )

    logger.debug("STYC series parsed: {} standings", len(standings))
    return standings


def _parse_race_page(race_html: str, source_id: str, race_num: int) -> list[RaceData]:
    """Parse one raceN.htm into per-class RaceData."""
    tree = HTMLParser(race_html)

    race_date = _extract_race_date(race_html)
    races: list[RaceData] = []

    class_titles = tree.css("p.classtitle")
    data_tables = [
        t
        for t in tree.css("table")
        if t.css("th")
        and "sailno" in " ".join(_decode(th.text(strip=True)).lower() for th in t.css("th"))
    ]

    for idx, table in enumerate(data_tables):
        title_text = _decode(class_titles[idx].text(strip=True)) if idx < len(class_titles) else ""
        class_name = _extract_class_name(title_text)
        start_time_str = _extract_start_time(title_text)

        rows = table.css("tr")
        if len(rows) < 2:
            continue

        headers = [_decode(th.text(strip=True)).lower() for th in rows[0].css("th")]
        col = _header_map(headers)
        finishes: list[BoatFinish] = []

        for row in rows[1:]:
            cells = row.css("td")
            if len(cells) < len(headers):
                continue

            sail = _decode(cells[col["sailno"]].text(strip=True))
            if not sail:
                continue

            boat_name = _cell_str(cells, col, "boatname")
            skipper = _cell_str(cells, col, "skipper")
            boat_type = _cell_str(cells, col, "boattype")
            yacht_club = _cell_str(cells, col, "club")
            rating_key = "rating" if "rating" in col else "ratinglink"
            rating = _cell_int(cells, col, rating_key)

            finish_raw = _cell_str(cells, col, "finish") or ""
            elapsed_raw = _cell_str(cells, col, "elapsed") or ""
            corrected_raw = _cell_str(cells, col, "corrected") or ""
            place_raw = _cell_str(cells, col, "place") or ""

            status = _extract_status(finish_raw)
            elapsed_s = _time_to_seconds(elapsed_raw)
            corrected_s = _time_to_seconds(corrected_raw)

            finishes.append(
                BoatFinish(
                    sail_number=sail,
                    boat_name=boat_name,
                    skipper=skipper,
                    boat_type=boat_type,
                    yacht_club=yacht_club,
                    phrf_rating=rating,
                    finish_time=finish_raw if finish_raw and not status else None,
                    start_time=start_time_str or None,
                    elapsed_seconds=elapsed_s,
                    corrected_seconds=corrected_s,
                    place=_parse_int(place_raw),
                    status_code=status,
                    fleet=class_name,
                )
            )

        if finishes:
            races.append(
                RaceData(
                    source_id=f"{source_id}_R{race_num}_{class_name}",
                    race_number=race_num,
                    name=f"Race {race_num}",
                    date=race_date,
                    class_name=class_name,
                    finishes=tuple(finishes),
                )
            )

    logger.debug("STYC race {} parsed: {} classes", race_num, len(races))
    return races


