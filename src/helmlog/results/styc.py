_STATUS_CODES = {"DNC", "DNS", "DNF", "DSQ", "RET", "OCS", "BFD", "UFD", "TLE", "NSC", "DNE"}


def _header_map(headers: list[str]) -> dict[str, int]:
    """Map normalized header names to column indices."""
    mapping: dict[str, int] = {}
    for i, h in enumerate(headers):
        h_clean = re.sub(r"\s+", "", h)
        if "boatname" in h_clean:
            mapping["boatname"] = i
        elif "sailno" in h_clean:
            mapping["sailno"] = i
        elif "boattype" in h_clean:
            mapping["boattype"] = i
        elif "skipper" in h_clean:
            mapping["skipper"] = i
        elif h_clean in ("rating", "ratinglink"):
            mapping["rating"] = i
        elif "club" in h_clean:
            mapping["club"] = i
        elif h_clean == "start":
            mapping["start"] = i
        elif h_clean == "finish":
            mapping["finish"] = i
        elif h_clean == "elapsed":
            mapping["elapsed"] = i
        elif "corrected" in h_clean:
            mapping["corrected"] = i
        elif h_clean == "place":
            mapping["place"] = i
        elif h_clean == "total":
            mapping["total"] = i
        elif h_clean in ("oa", "phrfoa"):
            mapping["oa"] = i
    return mapping


def _extract_race_date(race_html: str) -> str:
    """Extract date from ``<p class=racedate>Apr 14, 2025</p>``."""
    m = re.search(r"class=racedate>([^<]+)<", race_html)
    if not m:
        return ""
    try:
        dt = datetime.strptime(m.group(1).strip(), "%b %d, %Y")
        return dt.strftime("%Y-%m-%d")
    except ValueError:
        return ""


def _extract_class_name(title: str) -> str:
    """Extract class name from title like 'Class: 1 - Non-Flying Sails Division - Start: ...'"""
    m = re.match(r"Class:?\s*(.+?)(?:\s*-\s*Start:|\s*$)", title)
    if m:
        return m.group(1).strip().rstrip(" -")
    return title.strip()


def _extract_start_time(title: str) -> str:
    """Extract start time from title like '... - Start: 18:15:00 - ...'"""
    m = re.search(r"Start:\s*(\d{1,2}:\d{2}:\d{2})", title)
    return m.group(1) if m else ""


def _extract_status(text: str) -> str | None:
    """Extract status code from finish cell (e.g. 'DNC', 'DNF', 'RET')."""
    upper = text.upper().strip()
    for code in _STATUS_CODES:
        if code in upper:
            return code
    return None


def _time_to_seconds(text: str) -> int | None:
    """Parse H:MM:SS or M:SS to total seconds."""
    text = text.strip()
    if not text:
        return None
    parts = text.split(":")
    try:
        if len(parts) == 3:
            return int(parts[0]) * 3600 + int(parts[1]) * 60 + int(parts[2])
        if len(parts) == 2:
            return int(parts[0]) * 60 + int(parts[1])
    except ValueError:
        pass
    return None


def _parse_int(text: str) -> int | None:
    text = text.strip()
    if not text:
        return None
    try:
        return int(text)
    except ValueError:
        return None


def _parse_float(text: str) -> float | None:
    text = text.strip()
    if not text:
        return None
    try:
        return float(text)
    except ValueError:
        return None
